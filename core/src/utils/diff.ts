// ─── Helpers ──────────────────────────────────────────────────────────────────

/** Typed entry builder — casts getId/getLabel to (unknown) => string internally. */
function def<T>(
  key: keyof BEP,
  getId: (item: T) => string,
  getLabel: (item: T) => string,
): { key: keyof BEP; getId: (i: unknown) => string; getLabel: (i: unknown) => string } {
  return { key, getId: i => getId(i as T), getLabel: i => getLabel(i as T) }
}

