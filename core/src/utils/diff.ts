// ─── Core diff functions ──────────────────────────────────────────────────────

/** Pure: compares two entity arrays and returns what was added, removed, or modified. */
export function diffEntities<T>(
  baseArr: T[],
  currArr: T[],
  getId: (item: T) => string,
  getLabel: (item: T) => string,
): SectionDiff {
  const baseMap = new Map(baseArr.map(i => [getId(i), i]))
  const currMap = new Map(currArr.map(i => [getId(i), i]))
  return {
    added:    currArr.filter(i => !baseMap.has(getId(i))).map(getLabel),
    removed:  baseArr.filter(i => !currMap.has(getId(i))).map(getLabel),
    modified: currArr
      .filter(i => {
        const b = baseMap.get(getId(i))
        return b !== undefined && JSON.stringify(b) !== JSON.stringify(i)
      })
      .map(getLabel),
  }
}

/**
 * Pure: compares two BEP states (current vs base) and returns a structured diff.
 * Standards .md content changes are not detected here — they require zip access
 * and will be handled by the Standards entity when implemented.
 */
export function diffBep(current: BEP, base: BEP): BepDiff {
  // Project object
  let project: BepDiff['project'] = null
  if (JSON.stringify(current.project) !== JSON.stringify(base.project)) {
    const fields = Object.keys({ ...current.project, ...base.project }).filter(k => {
      const a = (current.project as unknown as Record<string, unknown>)[k]
      const b = (base.project as unknown as Record<string, unknown>)[k]
      return JSON.stringify(a) !== JSON.stringify(b)
    })
    project = { changed: true, fields }
  }

  // Entity arrays
  const sections: Record<string, SectionDiff> = {}
  for (const { key, getId, getLabel } of arrayDefs) {
    const baseArr = (base[key] as unknown[]) ?? []
    const currArr = (current[key] as unknown[]) ?? []
    const diff = diffEntities(baseArr, currArr, getId, getLabel)
    if (diff.added.length || diff.removed.length || diff.modified.length)
      sections[key] = diff
  }

  const changedKeys = [...(project ? ['project'] : []), ...Object.keys(sections)]
  return { project, sections, changedKeys }
}
